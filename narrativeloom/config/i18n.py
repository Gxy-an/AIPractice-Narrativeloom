def T(key: str, lang: str | None = None) -> str:
    lg = lang or "zh"
    if lg not in STRINGS:
        lg = "zh"
    return STRINGS[lg].get(key, STRINGS["zh"].get(key, key))


def normalize_persona_pool(p: Any) -> str:
    """统一为 genre / function。"""
    if p in ("genre", "function"):
        return p
    if p in ("题材人格", "类型化人格"):
        return "genre"
    if p in ("职能人格", "功能化人格"):
        return "function"
    return "genre"
